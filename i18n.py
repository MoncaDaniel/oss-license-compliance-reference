def get_translator(lang: str):
    """Return a t(key) function bound to the given locale, falling back to
    English for any missing key rather than crashing the demo."""
    table = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
    fallback = TRANSLATIONS["en"]

    def t(key: str) -> str:
        return table.get(key, fallback.get(key, key))

    return t
